'use client';

import { ArrowLeft, FloppyDisk, SpinnerGap } from '@phosphor-icons/react';
import Link from 'next/link';
import { useParams, useRouter } from 'next/navigation';
import * as React from 'react';

import { AdminPageHeader } from '@/components/admin/layout/admin-page-header';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Checkbox } from '@/components/ui/checkbox';
import { Container } from '@/components/ui/container';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select';
import { Skeleton } from '@/components/ui/skeleton';
import { Textarea } from '@/components/ui/textarea';
import { ImageUpload } from '@/components/admin/content/image-upload';
import { VideoUpload } from '@/components/admin/content/video-upload';
import {
  useAdminContentDetail,
  useUpdateContent,
} from '@/hooks/use-admin-content';

/**
 * Admin content edit page
 */
export default function AdminContentEditPage() {
  const params = useParams();
  const router = useRouter();
  const contentId = params.id as string;

  const { data: content, isLoading } = useAdminContentDetail(contentId);
  const updateContent = useUpdateContent();

  // Form state
  const [title, setTitle] = React.useState('');
  const [slug, setSlug] = React.useState('');
  const [description, setDescription] = React.useState('');
  const [contentType, setContentType] = React.useState('');
  const [categoryId, setCategoryId] = React.useState('');
  const [ageCategory, setAgeCategory] = React.useState('');
  const [thumbnailUrl, setThumbnailUrl] = React.useState('');
  const [previewUrl, setPreviewUrl] = React.useState('');
  const [isFree, setIsFree] = React.useState(false);
  const [individualPrice, setIndividualPrice] = React.useState('');
  const [status, setStatus] = React.useState('');

  // Populate form when data loads
  React.useEffect(() => {
    if (content) {
      setTitle(content.title || '');
      setSlug(content.slug || '');
      setDescription(content.description || '');
      setContentType(content.contentType || '');
      setCategoryId(content.categoryId || '');
      setAgeCategory(content.ageCategory || '');
      setThumbnailUrl(content.thumbnailUrl || '');
      setPreviewUrl(content.previewUrl || '');
      setIsFree(content.isFree || false);
      setIndividualPrice(content.individualPrice?.toString() || '');
      setStatus(content.status || '');
    }
  }, [content]);

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();

    updateContent.mutate(
      {
        id: contentId,
        title,
        slug: slug || undefined,
        description: description || undefined,
        contentType: contentType || undefined,
        categoryId: categoryId || undefined,
        ageCategory: ageCategory || undefined,
        thumbnailUrl: thumbnailUrl || undefined,
        previewUrl: previewUrl || undefined,
        isFree,
        individualPrice: individualPrice ? Number(individualPrice) : undefined,
        status: status || undefined,
      },
      {
        onSuccess: () => {
          router.push('/admin/content');
        },
      }
    );
  };

  if (isLoading) {
    return (
      <Container size="xl" className="py-8">
        <div className="mb-6">
          <Skeleton className="h-8 w-48" />
        </div>
        <Card>
          <CardContent className="p-6 space-y-6">
            {Array.from({ length: 6 }).map((_, i) => (
              <div key={i} className="space-y-2">
                <Skeleton className="h-4 w-24" />
                <Skeleton className="h-10 w-full" />
              </div>
            ))}
          </CardContent>
        </Card>
      </Container>
    );
  }

  if (!content) {
    return (
      <Container size="xl" className="py-8">
        <Card className="py-12 text-center">
          <p className="text-mp-text-secondary">Контент не найден</p>
          <Button variant="outline" className="mt-4" asChild>
            <Link href="/admin/content">
              <ArrowLeft className="mr-2 h-4 w-4" />
              Вернуться к списку
            </Link>
          </Button>
        </Card>
      </Container>
    );
  }

  return (
    <Container size="xl" className="py-8">
      <div className="mb-6">
        <Button variant="ghost" size="sm" asChild>
          <Link href="/admin/content">
            <ArrowLeft className="mr-2 h-4 w-4" />
            Назад к списку
          </Link>
        </Button>
      </div>

      <AdminPageHeader
        title={content.title}
        description={`Редактирование контента`}
        breadcrumbItems={[
          { label: 'Контент', href: '/admin/content' },
          { label: content.title },
        ]}
      />

      <form onSubmit={handleSubmit}>
        <div className="grid gap-6 lg:grid-cols-3 mt-6">
          {/* Main fields */}
          <div className="lg:col-span-2 space-y-6">
            <Card>
              <CardHeader>
                <CardTitle className="text-lg">Основная информация</CardTitle>
              </CardHeader>
              <CardContent className="space-y-4">
                <div className="space-y-2">
                  <Label htmlFor="title">Название</Label>
                  <Input
                    id="title"
                    value={title}
                    onChange={(e) => setTitle(e.target.value)}
                    placeholder="Введите название"
                    required
                  />
                </div>

                <div className="space-y-2">
                  <Label htmlFor="slug">Slug (URL)</Label>
                  <Input
                    id="slug"
                    value={slug}
                    onChange={(e) => setSlug(e.target.value)}
                    placeholder="auto-generated-slug"
                  />
                </div>

                <div className="space-y-2">
                  <Label htmlFor="description">Описание</Label>
                  <Textarea
                    id="description"
                    value={description}
                    onChange={(e) => setDescription(e.target.value)}
                    placeholder="Введите описание контента..."
                    rows={5}
                  />
                </div>
              </CardContent>
            </Card>

            <Card>
              <CardHeader>
                <CardTitle className="text-lg">Медиа</CardTitle>
              </CardHeader>
              <CardContent className="space-y-4">
                <ImageUpload
                  label="Обложка"
                  description="Изображение обложки контента"
                  value={thumbnailUrl}
                  onChange={setThumbnailUrl}
                />

                <VideoUpload
                  label="Превью видео"
                  description="Короткое превью контента"
                  value={previewUrl}
                  onChange={setPreviewUrl}
                />
              </CardContent>
            </Card>

            <Card>
              <CardHeader>
                <CardTitle className="text-lg">Видео контент</CardTitle>
              </CardHeader>
              <CardContent>
                <VideoUpload
                  contentId={contentId}
                  label="Основное видео"
                  description="Загрузите видео для транскодирования в HLS (MP4, WebM, MOV, MKV до 5GB)"
                  accept="video/mp4,video/webm,video/quicktime,video/x-matroska"
                  maxSizeMB={5120}
                  onChange={() => {}}
                />
              </CardContent>
            </Card>
          </div>

          {/* Sidebar fields */}
          <div className="space-y-6">
            <Card>
              <CardHeader>
                <CardTitle className="text-lg">Настройки</CardTitle>
              </CardHeader>
              <CardContent className="space-y-4">
                <div className="space-y-2">
                  <Label>Тип контента</Label>
                  <Select value={contentType} onValueChange={setContentType}>
                    <SelectTrigger>
                      <SelectValue placeholder="Выберите тип" />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value="SERIES">Сериал</SelectItem>
                      <SelectItem value="CLIP">Клип</SelectItem>
                      <SelectItem value="SHORT">Шорт</SelectItem>
                      <SelectItem value="TUTORIAL">Туториал</SelectItem>
                    </SelectContent>
                  </Select>
                </div>

                <div className="space-y-2">
                  <Label>Категория возраста</Label>
                  <Select value={ageCategory} onValueChange={setAgeCategory}>
                    <SelectTrigger>
                      <SelectValue placeholder="Выберите возраст" />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value="0+">0+</SelectItem>
                      <SelectItem value="6+">6+</SelectItem>
                      <SelectItem value="12+">12+</SelectItem>
                      <SelectItem value="16+">16+</SelectItem>
                      <SelectItem value="18+">18+</SelectItem>
                    </SelectContent>
                  </Select>
                </div>

                <div className="space-y-2">
                  <Label>Статус</Label>
                  <Select value={status} onValueChange={setStatus}>
                    <SelectTrigger>
                      <SelectValue placeholder="Выберите статус" />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value="DRAFT">Черновик</SelectItem>
                      <SelectItem value="PENDING">На модерацию</SelectItem>
                      <SelectItem value="PUBLISHED">Опубликован</SelectItem>
                      <SelectItem value="REJECTED">Отклонён</SelectItem>
                      <SelectItem value="ARCHIVED">Архив</SelectItem>
                    </SelectContent>
                  </Select>
                </div>

                <div className="space-y-2">
                  <Label htmlFor="categoryId">ID категории</Label>
                  <Input
                    id="categoryId"
                    value={categoryId}
                    onChange={(e) => setCategoryId(e.target.value)}
                    placeholder="UUID категории"
                  />
                </div>
              </CardContent>
            </Card>

            <Card>
              <CardHeader>
                <CardTitle className="text-lg">Монетизация</CardTitle>
              </CardHeader>
              <CardContent className="space-y-4">
                <div className="flex items-center space-x-2">
                  <Checkbox
                    id="isFree"
                    checked={isFree}
                    onCheckedChange={(checked) => setIsFree(checked === true)}
                  />
                  <Label htmlFor="isFree">Бесплатный контент</Label>
                </div>

                {!isFree && (
                  <div className="space-y-2">
                    <Label htmlFor="individualPrice">Цена (руб.)</Label>
                    <Input
                      id="individualPrice"
                      type="number"
                      value={individualPrice}
                      onChange={(e) => setIndividualPrice(e.target.value)}
                      placeholder="0"
                      min="0"
                      step="1"
                    />
                  </div>
                )}
              </CardContent>
            </Card>

            {/* Actions */}
            <Card>
              <CardContent className="p-4 space-y-2">
                <Button
                  type="submit"
                  className="w-full"
                  disabled={updateContent.isPending}
                >
                  {updateContent.isPending ? (
                    <SpinnerGap className="mr-2 h-4 w-4 animate-spin" />
                  ) : (
                    <FloppyDisk className="mr-2 h-4 w-4" />
                  )}
                  Сохранить
                </Button>
                <Button variant="outline" className="w-full" asChild>
                  <Link href="/admin/content">Отмена</Link>
                </Button>
              </CardContent>
            </Card>
          </div>
        </div>
      </form>
    </Container>
  );
}
